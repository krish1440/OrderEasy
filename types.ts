/**
 * Response structure for AI-driven revenue forecasting.
 */
export interface ForecastResponse {
  /** The statistical R-squared score of the model. */
  r2_score: number;
  /** Qualitative confidence string (e.g., "High", "Medium"). */
  confidence_level: string;
  /** Historical monthly revenue data used for the baseline. */
  historical_data: {
    month: string;
    revenue: number;
  }[];
  /** Predicted revenue for the next 12 months with bounds. */
  forecast_12_months: {
    month: string;
    predicted_revenue: number;
    lower_bound: number;
    upper_bound: number;
  }[];
}

