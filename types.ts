/**
 * Represents a user profile within the OrderEazy system.
 */
export interface User {
  /** The unique username of the user. */
  username: string;
  /** The name of the organization the user belongs to. */
  organization: string;
  /** Whether the user has administrative privileges. */
  is_admin: boolean;
  /** The email address of the user (optional). */
  email?: string;
  /** A URL pointing to the user's or organization's logo image (optional). */
  logo_url?: string;
}

/**
 * Represents a sales order in the system.
 */
export interface Order {
  /** Unique identifier for the order. */
  order_id: number;
  /** The organization that placed or owns the order. */
  org: string;
  /** Name of the person or entity receiving the order. */
  receiver_name: string;
  /** The date the order was placed (Format: YYYY-MM-DD). */
  date: string;
  /** The expected date for order fulfillment (Format: YYYY-MM-DD). */
  expected_delivery_date: string;
  /** The name or ID of the product ordered. */
  product: string;
  /** Additional details or notes about the order (optional). */
  description?: string;
  /** Total units of the product ordered. */
  quantity: number;
  /** Selling price per unit. */
  price: number;
  /** Base price before taxes or markings. */
  basic_price: number;
  /** GST amount applied to the order. */
  gst: number;
  /** Amount paid by the customer in advance. */
  advance_payment: number;
  /** The final total amount including GST. */
  total_amount_with_gst: number;
  /** The remaining balance to be paid. */
  pending_amount: number;
  /** Current fulfillment status. */
  status: "Completed" | "Pending";
  /** Total units successfully delivered so far. */
  delivered_quantity: number;
  /** URL to any associated electronic document or invoice (optional). */
  url?: string;
  /** The username of the person who created this order (optional). */
  created_by?: string;
  /** Dynamic key-value pairs for business-specific data (optional). */
  custom_data?: Record<string, string>;
}

/**
 * Represents a specific delivery associated with an order.
 */
export interface Delivery {
  /** The ID of the parent order. */
  order_id: number;
  /** Unique identifier for this specific delivery shipment. */
  delivery_id: number;
  /** The organization handling the delivery. */
  org: string;
  /** Quantity of units delivered in this specific shipment. */
  delivery_quantity: number;
  /** The date the delivery was made (Format: YYYY-MM-DD). */
  delivery_date: string;
  /** Total payment received during this delivery. */
  total_amount_received: number;
  /** Cloudinary or external asset unique identifier (optional). */
  public_id?: string;
  /** URL to the delivery slip or proof of delivery (optional). */
  url?: string;
  /** Original filename of the uploaded document (optional). */
  file_name?: string;
  /** Timestamp when the delivery record/file was uploaded (optional). */
  upload_date?: string;
  /** Type of resource uploaded (e.g., 'image', 'pdf') (optional). */
  resource_type?: string;
  /** Dynamic metadata for the delivery (optional). */
  custom_data?: Record<string, string>;
}

